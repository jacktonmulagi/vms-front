const defaultTheme = createTheme();

function validateEmail(email) {
  // Email validation regular expression
  const emailRegex = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
  return emailRegex.test(email);
}

function validatePassword(password) {
  // You can add your own password validation logic here
  // For example, check for minimum length, special characters, etc.
  return password.length >= 6;
}

function isValidFormData(formData) {
  const { email, password } = formData;
  return validateEmail(email) && validatePassword(password);
}

export default function SignIn() {
  const [formData, setFormData] = React.useState({
    email: '',
    password: '',
  });
  

  const handleSubmit = async (event) => {
    event.preventDefault();

    if (!isValidFormData(formData)) {
      // If the form data is not valid, show an error message or prevent form submission.
      console.error('Invalid form data');
      return;
    }

    try {
      const response = await instance.post('/auth/sign-in', formData);
      console.log('User sign-in successful:', response.data);

      // Save the response to session storage
      sessionStorage.setItem('userResponse', JSON.stringify(response.data));

      // You can redirect to another page if needed
      window.location.href = '/book-appointment';
    } catch (error) {
      console.error('User sign-in failed:', error);
    }
  };

  const handleChange = (event) => {
    const { name, value } = event.target;
    setFormData({ ...formData, [name]: value });
  };

  return (
    <ThemeProvider theme={defaultTheme}>
      <Container component="main" maxWidth="xs">
        <CssBaseline />
        <Box
          sx={{
            marginTop: 8,
            display: 'flex',
            flexDirection: 'column',
            alignItems: 'center',
          }}
        >
          <Avatar sx={{ m: 1, bgcolor: 'secondary.main' }}>
            <LockOutlinedIcon />
          </Avatar>
          <Typography component="h1" variant="h5">
            Sign in
          </Typography>
          <Box component="form" onSubmit={handleSubmit} noValidate sx={{ mt: 1 }}>
            <TextField
              margin="normal"
              required
              fullWidth
              id="email"
              label="Email Address"
              name="email"
              autoComplete="email"
              autoFocus
              value={formData.email}
              onChange={handleChange}
            />
            <TextField
              margin="normal"
              required
              fullWidth
              name="password"
              label="Password"
              type="password"
              id="password"
              autoComplete="current-password"
              value={formData.password}
              onChange={handleChange}
            />
            <FormControlLabel
              control={<Checkbox value="remember" color="primary" />}
              label="Remember me"
            />
            <Button
              type="submit"
              fullWidth
              variant="contained"
              sx={{ mt: 3, mb: 2 }}
            >
              Sign In
            </Button>
            <Grid container>
              <Grid item xs>
                <Link href="#" variant="body2">
                  Forgot password?
                </Link>
              </Grid>
              <Grid item>
                <Link href="/SignUp" variant="body2">
                  {"Don't have an account? Sign Up"}
                </Link>
              </Grid>
            </Grid>
          </Box>
        </Box>
        <Copyright sx={{ mt: 8, mb: 4 }} />
      </Container>
    </ThemeProvider>
  );
}